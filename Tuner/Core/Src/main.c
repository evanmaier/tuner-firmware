/* USER CODE BEGIN 0 */
void fft()
{
	float magnitudes [FFT_BUFFER_SIZE/2];
	float peakVal = 0.0f;

	// initialize fft function
	arm_rfft_fast_init_f32(&fftHandler, FFT_BUFFER_SIZE);

	// run fft calculation
	arm_rfft_fast_f32(&fftHandler, fftBufIn, fftBufOut, 0);

	// calculate magnitudes
	arm_cmplx_mag_f32(fftBufOut, magnitudes, FFT_BUFFER_SIZE/2);

	// identify peak frequency
	for(int i = 0; i < FFT_BUFFER_SIZE/2; i++) {
		if(magnitudes[i] > peakVal){
			peakVal = magnitudes[i];
			peakHz = ((float)i * SAMPLE_FREQ) / (float)FFT_BUFFER_SIZE;
		}
	}
}

void updateDisplay(float fn)
{
	// number of semitones from A4
	float n = 12.0 * log2f(fn/F0);
	float modn = fmodf(n, 12.0f);
	switch ((int)roundf(modn))
	{
	case 0:
		ST7735_WriteString(0,0,"A", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 1:
		ST7735_WriteString(0,0,"A#", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 2:
		ST7735_WriteString(0,0,"B", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 3:
		ST7735_WriteString(0,0,"C", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 4:
		ST7735_WriteString(0,0,"C#", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 5:
		ST7735_WriteString(0,0,"D", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 6:
		ST7735_WriteString(0,0,"D#", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 7:
		ST7735_WriteString(0,0,"E", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 8:
		ST7735_WriteString(0,0,"F", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 9:
		ST7735_WriteString(0,0,"F#", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 10:
		ST7735_WriteString(0,0,"G", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	case 11:
		ST7735_WriteString(0,0,"G#", Font_16x26, ST7735_RED, ST7735_BLACK);
		break;
	}
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	// normalize ADC data for FFT
	for(int i=0; i < FFT_BUFFER_SIZE; i++) {
	  fftBufIn[i] = (float32_t)(adcData[i] - 2048) / 2048.0f;
	}
	if(TIM3Flag){
		fft();
		updateDisplay(peakHz);
		TIM3Flag = 0;
	}

}

void HAL_TIM_TriggerCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Instance == TIM3) {
		TIM3Flag = 1;
	}
}
